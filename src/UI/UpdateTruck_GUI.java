    private void okayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okayActionPerformed
        // TODO add your handling code here:
       updatedtruckinfo.setText("");
        if(newmake.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if( newmodel.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if(newlicense.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (newfitreg.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if(newreg.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if(newins.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        } else{
            Truck[] newtruck = Truck.UpdateTruckInfo(Integer.parseInt(truckid_u.getText()),newmake.getText(), newmodel.getText(), newlicense.getText(), newfitreg.getText(), newreg.getText(), newins.getText());
            Truck tr=newtruck[0];
            updatedtruckinfo.setText(tr.toString());
            JOptionPane.showMessageDialog(null, "Truck Successfully Updated!");

        }
    }//GEN-LAST:event_okayActionPerformed

