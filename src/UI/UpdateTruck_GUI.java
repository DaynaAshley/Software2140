    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        truckinfo.setText("");
        if (!truckid_u.getText().isEmpty()){
            Truck[] truck = Truck.FindTruck(Integer.parseInt(truckid_u.getText()));
            if (truck[0]!= null){
                Truck tr=truck[0];
                truckinfo.setText(tr.toString());
            }
            else{        
            JOptionPane.showMessageDialog(null, "Truck NOT FOUND!!");
            JOptionPane.showMessageDialog(null, "Returning to the Menu");
            setVisible(false);
            TruckManagement_GUI newmenu= new TruckManagement_GUI();
            
            newmenu.setVisible(true);  

            }
            
        }else{
            JOptionPane.showMessageDialog(null, "ID Field Empty");
        }

       
    }//GEN-LAST:event_submitActionPerformed

