    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        info.setText("");
        if (!truckid_d.getText().isEmpty()){
            int idd= Integer.parseInt(truckid_d.getText());
            Truck [] truck= Truck.FindTruck(idd);
            if (truck[0]==null){
                    JOptionPane.showMessageDialog(null, "Truck NOT FOUND!!");
                }
                else{
                    info.setText(truck[0].toString());
                }
        }
        else{
             JOptionPane.showMessageDialog(null, "ID Field Empty");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

