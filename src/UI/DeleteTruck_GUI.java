    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        int idd= Integer.parseInt(truckid_d.getText());
        String cho=choice.getSelectedItem().toString();
        if (cho.equals("Yes")){
            int status=Truck.DelTruck(idd);
            if (status==0){
                    JOptionPane.showMessageDialog(null, "Truck ID entered does not exist!");
            }
            else{
                JOptionPane.showMessageDialog(null, "Truck successfully deleted!");
                this.dispose();
                TruckManagement_GUI newtruck= new TruckManagement_GUI();
                newtruck.setVisible(true);
                }   
        }
            
        else{
                JOptionPane.showMessageDialog(null, "Cancelling Delete!");
                JOptionPane.showMessageDialog(null, "Returning to the Menu");
                this.dispose();
                TruckManagement_GUI newmenu= new TruckManagement_GUI();
                newmenu.setVisible(true);
            }
            


    }//GEN-LAST:event_submitActionPerformed

