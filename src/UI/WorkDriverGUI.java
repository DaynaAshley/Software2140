    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Route>r=RouteManager.RouteNames(company.getText());
        if (r.isEmpty()){
                JOptionPane.showMessageDialog(null,"Company not Found!");
                JOptionPane.showMessageDialog(null,"Enter new Company!");
            }
            else{
                for(Route i: r){
                    destname.addItem(i.getDestName());
                }
                
            }
        
        ArrayList<Truck>truck=Truck.AvaliableTrucks();
        if (truck.isEmpty()){
            JOptionPane.showMessageDialog(null,"No Trucks Available!");
        }
        else{
            for (Truck t:truck){
                truckid.addItem(String.valueOf(t.getTruckID()));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Driver[] driver=Driver.FindDriver(Integer.parseInt(id.getText()));
        if (driver[0]==null){
            JOptionPane.showMessageDialog(null,"Driver ID does not exist!\nEnter new id!");
        }
        else{
            name.setText(driver[0].getName());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

