    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        
        if (company.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (goods.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (id.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (side1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (side2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }

        else{
            DateFormat df = new SimpleDateFormat("dd/MM/yy");
            Date dateobj = new Date();
            String date= df.format(dateobj);

            int idd= Integer.parseInt(id.getText());
            double wght =Double.parseDouble(weight.getText()) ;
            int truck_id= Integer.parseInt(String.valueOf(truckid.getSelectedItem()));
            DriverWork work= new DriverWork(name.getText(),idd,company.getText(),String.valueOf(destname.getSelectedItem()),goods.getText(),wght,side1.getText(),side2.getText(),truck_id,date);
            Driver.DriverWork(work);

            log.setText(work.toString());
        }
    }//GEN-LAST:event_submitActionPerformed

