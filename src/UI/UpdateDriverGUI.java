    private void okayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okayActionPerformed
        // TODO add your handling code here:
        if (name.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (address.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (license.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (email.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else if (telnum.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Field Present!!");
        }
        else{
            int idd= Integer.parseInt(id.getText());
            Driver [] driver1=Driver.UpdateDriver(idd,name.getText(),telnum.getText(),address.getText(),license.getText(),email.getText());
            Driver dr1=driver1[0];
            upinfo.setText(dr1.toString());
            JOptionPane.showMessageDialog(null, "Driver Successfully Updated!");
        }
    }//GEN-LAST:event_okayActionPerformed

